--Creating table for orders

CREATE TABLE orders(
	order_id INT NOT NULL PRIMARY KEY,
	order_date DATE NOT NULL,
	order_time TIME NOT NULL
)

SELECT * FROM orders;


--Creating table for Pizzas

CREATE TABLE pizzas(
	pizza_id VARCHAR(100) NOT NULL PRIMARY KEY,
	pizza_type_id VARCHAR(100) NOT NULL,
	pizza_size VARCHAR(10) NOT NULL,
	pizza_price NUMERIC(10, 2)
)

SELECT * FROM pizzas;


--Creating table for Pizza types

CREATE TABLE pizza_types (
    pizza_type_id VARCHAR(50) PRIMARY KEY,
    pizza_name VARCHAR(100) NOT NULL,
    category VARCHAR(50) NOT NULL,
    ingredients TEXT NOT NULL
);


SELECT * FROM pizza_types;


--Creating table for Order Details

CREATE TABLE order_details(
	order_details_id INT NOT NULL PRIMARY KEY,
	order_id INT NOT NULL,
	pizza_id VARCHAR(100) NOT NULL,
	order_quantity INT NOT NULL
)

SELECT * FROM order_details;


